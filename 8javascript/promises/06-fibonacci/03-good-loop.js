// =============================================================================
const fib = (n) => {
  if (n === 0 || n === 1) return n;

  let prev1 = 0;
  let prev2 = 1;
  let cur = null;

  for (let i = 2; i <= n; i++) {
    cur = prev1 + prev2;
    prev1 = prev2;
    prev2 = cur;
  }

  return cur;
}



// =============================================================================
// Вызов функции
// =============================================================================
console.log(fib(5))
console.log(fib(40))

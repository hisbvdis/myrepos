// =============================================================================
// Рекурсивная функция
// =============================================================================
const power = (x, n) => {
  // 1. Простой случай
  // 1.1. Если степень "n" === 1 (то есть, "x" умножатся на 1)
  if (n === 1) {
    // 1.2. Вернуть само значение "x"
    return x;
  }
  
  // 2. Сложный случай
  // 2.1. Вызвать функцию со степенью на 1 меньше
  // 2.2. Результат вызова умножить на "x"
  return x * power(x, n - 1);
}



// =============================================================================
// Вызов функции
// =============================================================================
console.log( "power(2, 2):", power(2, 2) );
console.log( "power(2, 3):", power(2, 3) );
console.log( "power(2, 4):", power(2, 4) );

export const average = (array) => {
  return array.reduce((sum, value) => sum + value) / array.length;
}



// ===========================================================================
// ГРУППИРОВКА ЗНАЧЕНИЙ МАССИВА В ОБЪЕКТЕ
// ===========================================================================
export const groupValues = (array) => {
  return array.reduce(
    (accum, value) => ({
      // Spread-оператор (достать свойства объекта)
      ...accum,
      // Добавить новое свойство или перезаписать существующее свойство,
      // ...название которого содержится в переменной "value"
      // Значение — текущее значение (если свойство уже есть) или "0"
      // Прибавить к свойству "1"
      [value]: (accum[value] || 0) + 1,
    }),
    {}
  );
}




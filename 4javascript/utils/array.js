// ===========================================================================
// РАЗБИВКА МАССИВА НА РАВНЫЕ ГРУППЫ
// ===========================================================================
const divideIntoGroups = (arr, L) => {
  return arr.reduce((acc, _, i, arr) => (!(i % L) ? [...acc, arr.slice(i, i + L)] : acc), []);
}

console.log( divideIntoGroups([1, 2, 3, 4, 5, 6], 2) )
